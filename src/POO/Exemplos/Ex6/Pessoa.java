package POO.Exemplos.Ex6;

public class Pessoa {
    private String nome;
    private int RG;
    private int CPF;

    public String getNome(){
        return nome;
    }

    public void setNome(int CPF) {
        this.nome = nome;
    }

    public int getRG(){
        return RG;
    }

    public void setRG(int RG) {
        this.RG = RG;
    }

    public int getCPF(){
        return CPF;
    }

    public void setCPF(int CPF) {
        this.CPF = CPF;
    }
}
